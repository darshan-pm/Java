public class switchStatement1 {
    public static void main(String[] args) {
        int n = 4;
        switch (n) {
            case 1:
                System.out.println("Darshan");
                break;
            case 2:
                System.out.println("Pranav");
                break;
            case 3:
                System.out.println("Aakash");
                break;
            case 4:
                System.out.println("Koustub");
                break;
            case 5:
                System.out.println("Dhanush");
                break;
            default:
                System.out.println("Enter valid number:");

        }
    }

}
